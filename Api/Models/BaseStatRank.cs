namespace GearForgeXI.Models;

/// <summary>
/// ranking levels used in FFXI for base stat aptitudes
/// </summary>
public enum BaseStatRank
{
    /// <summary>
    /// A rank - highest base stat aptitude
    /// </summary>
    A,

    /// <summary>
    /// B rank - high base stat aptitude
    /// </summary>
    B,

    /// <summary>
    /// B+ rank - above average base stat aptitude
    /// </summary>
    C,

    /// <summary>
    /// D rank - average base stat aptitude
    /// </summary>
    D,

    /// <summary>
    /// E rank - below average base stat aptitude
    /// </summary>
    E,

    /// <summary>
    /// F rank - low base stat aptitude
    /// </summary>
    F,

    /// <summary>
    /// G rank - lowest base stat aptitude
    /// </summary>
    G,
}
