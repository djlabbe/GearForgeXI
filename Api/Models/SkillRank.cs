namespace GearForgeXI.Models;

/// <summary>
/// Skill ranking levels used in FFXI for job skill aptitudes
/// </summary>
public enum SkillRank
{
    /// <summary>
    /// A+ rank - highest skill aptitude
    /// </summary>
    APlus,

    /// <summary>
    /// A- rank - high skill aptitude
    /// </summary>
    AMinus,

    /// <summary>
    /// B+ rank - above average skill aptitude
    /// </summary>
    BPlus,

    /// <summary>
    /// B rank - average skill aptitude
    /// </summary>
    B,

    /// <summary>
    /// B- rank - below average skill aptitude
    /// </summary>
    BMinus,

    /// <summary>
    /// C+ rank - low skill aptitude
    /// </summary>
    CPlus,

    /// <summary>
    /// C rank - low skill aptitude
    /// </summary>
    C,

    /// <summary>
    /// C- rank - very low skill aptitude
    /// </summary>
    CMinus,

    /// <summary>
    /// D rank - very low skill aptitude
    /// </summary>
    D,

    /// <summary>
    /// E rank - extremely low skill aptitude
    /// </summary>
    E,

    /// <summary>
    /// F rank - lowest skill aptitude
    /// </summary>
    F
}
